const fs = require("node:fs");
const path = require("node:path");

function parseArg(name, fallback) {
  const flag = `--${name}`;
  const hit = process.argv.find((entry) => entry.startsWith(`${flag}=`));
  if (!hit) {
    return fallback;
  }
  return hit.slice(flag.length + 1);
}

function sanitizeSqlString(value) {
  return value.replace(/'/g, "''");
}

function buildDeleteSql(ids, generatedAt, sourcePath) {
  const idRows = ids.map((id) => `('${sanitizeSqlString(id)}')`).join(",\n");
  return `-- Generated by scripts/build-non-music-delete-sql.js\n-- Generated at ${generatedAt.toISOString()}\n-- Source ids file: ${sourcePath}\n-- Target count: ${ids.length}\n\nSTART TRANSACTION;\n\nDROP TEMPORARY TABLE IF EXISTS ytr_non_music_targets;\nCREATE TEMPORARY TABLE ytr_non_music_targets (\n  video_id VARCHAR(32) NOT NULL PRIMARY KEY\n);\n\nINSERT INTO ytr_non_music_targets (video_id) VALUES\n${idRows};\n\nSELECT COUNT(*) AS target_count FROM ytr_non_music_targets;\nSELECT COUNT(*) AS videos_found FROM videos v INNER JOIN ytr_non_music_targets t ON t.video_id = v.videoId;\n\nDELETE sv FROM site_videos sv\nINNER JOIN videos v ON v.id = sv.video_id\nINNER JOIN ytr_non_music_targets t ON t.video_id = v.videoId;\n\nDELETE av FROM videosbyartist av\nINNER JOIN videos v ON v.id = av.video_id\nINNER JOIN ytr_non_music_targets t ON t.video_id = v.videoId;\n\nDELETE pi FROM playlistitems pi\nINNER JOIN videos v ON v.id = pi.video_id\nINNER JOIN ytr_non_music_targets t ON t.video_id = v.videoId;\n\nDELETE f FROM favourites f\nINNER JOIN ytr_non_music_targets t ON t.video_id = f.videoId;\n\nDELETE m FROM messages m\nINNER JOIN ytr_non_music_targets t ON t.video_id = m.video_id;\n\nDELETE r FROM related r\nINNER JOIN ytr_non_music_targets t ON t.video_id = r.videoId OR t.video_id = r.related;\n\nDELETE v FROM videos v\nINNER JOIN ytr_non_music_targets t ON t.video_id = v.videoId;\n\nCOMMIT;\n`;
}

function main() {
  const inputPath = parseArg("input", "");
  const outputPathArg = parseArg("output", "");

  if (!inputPath) {
    throw new Error("Usage: node scripts/build-non-music-delete-sql.js --input=logs/non-music-approved-ids.txt [--output=logs/non-music-approved-vps-delete.sql]");
  }

  const resolvedInput = path.resolve(process.cwd(), inputPath);
  if (!fs.existsSync(resolvedInput)) {
    throw new Error(`Input file not found: ${inputPath}`);
  }

  const lines = fs.readFileSync(resolvedInput, "utf8").split(/\r?\n/);
  const ids = [...new Set(lines.map((line) => line.trim()).filter((line) => /^[A-Za-z0-9_-]{11}$/.test(line)))];

  if (ids.length === 0) {
    throw new Error("No valid 11-char video IDs found in input file.");
  }

  const now = new Date();
  const stamp = now.toISOString().replace(/[:.]/g, "-");
  const resolvedOutput = outputPathArg
    ? path.resolve(process.cwd(), outputPathArg)
    : path.resolve(process.cwd(), `logs/non-music-approved-${stamp}-vps-delete.sql`);

  fs.mkdirSync(path.dirname(resolvedOutput), { recursive: true });
  fs.writeFileSync(resolvedOutput, buildDeleteSql(ids, now, inputPath));

  console.log(`Input IDs: ${ids.length}`);
  console.log(`Output SQL: ${path.relative(process.cwd(), resolvedOutput)}`);
}

try {
  main();
} catch (error) {
  console.error(error instanceof Error ? error.message : String(error));
  process.exit(1);
}
