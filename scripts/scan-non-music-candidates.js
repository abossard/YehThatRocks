const fs = require("node:fs");
const path = require("node:path");
const { PrismaClient } = require("@prisma/client");

function loadDatabaseEnv() {
  const candidateEnvPaths = [
    path.resolve(process.cwd(), ".env.local"),
    path.resolve(process.cwd(), "apps/web/.env.local"),
    path.resolve(process.cwd(), ".env.production"),
    path.resolve(process.cwd(), "apps/web/.env.production"),
  ];

  for (const envPath of candidateEnvPaths) {
    if (!fs.existsSync(envPath)) {
      continue;
    }

    const lines = fs.readFileSync(envPath, "utf8").split(/\r?\n/);
    for (const line of lines) {
      const trimmed = line.trim();
      if (!trimmed || trimmed.startsWith("#")) {
        continue;
      }

      const match = trimmed.match(/^([A-Z0-9_]+)=(.*)$/);
      if (!match) {
        continue;
      }

      const [, key, rawValue] = match;
      if (process.env[key]) {
        continue;
      }

      process.env[key] = rawValue.replace(/^"/, "").replace(/"$/, "");
    }
  }
}

function parseArg(name, fallback) {
  const flag = `--${name}`;
  const hit = process.argv.find((entry) => entry.startsWith(`${flag}=`));
  if (!hit) {
    return fallback;
  }
  return hit.slice(flag.length + 1);
}

function escapeIdentifier(identifier) {
  return `\`${String(identifier).replace(/`/g, "``")}\``;
}

function pickColumn(available, candidates) {
  for (const candidate of candidates) {
    if (available.has(candidate)) {
      return candidate;
    }
  }

  return null;
}

function sanitizeSqlString(value) {
  return value.replace(/'/g, "''");
}

const NON_MUSIC_PATTERN = /\b(instagram|tiktok|facebook|whatsapp|snapchat|podcast|interview|prank|challenge|reaction|vlog|tutorial|gameplay|livestream|stream highlights?|news|fails?|compilation|meme|shorts?)\b/i;
const ALLOWED_VIDEO_TYPES = new Set(["official", "lyric", "live", "cover", "remix", "fan"]);
const LONG_FORM_MUSIC_MARKER = /\bfull\s+(ep|album)|\b(ep|album|demo|lp)\b/i;

function scoreCandidate(row) {
  const title = String(row.title || "");
  const description = String(row.description || "");
  const artist = String(row.parsedArtist || "").trim();
  const track = String(row.parsedTrack || "").trim();
  const videoType = String(row.parsedVideoType || "").trim().toLowerCase();
  const confidence = Number(row.parseConfidence ?? NaN);
  const parseMethod = String(row.parseMethod || "").trim().toLowerCase();
  const hasLongFormMusicMarker = LONG_FORM_MUSIC_MARKER.test(title);
  const hasMusicLikeMetadata = Boolean(artist) && Boolean(track);

  let score = 0;
  const reasons = [];
  const hasNonMusicKeyword = NON_MUSIC_PATTERN.test(`${title}\n${description}`);

  if (hasNonMusicKeyword) {
    score += 3;
    reasons.push("non-music-keyword");
  }

  if (!artist || !track) {
    score += 2;
    reasons.push("missing-artist-or-track");
  }

  const isLikelyMusicNoNonMusicSignal = hasMusicLikeMetadata && !hasNonMusicKeyword;
  const shouldTreatMissingTypeAsAcceptable = !videoType && hasMusicLikeMetadata && (hasLongFormMusicMarker || !hasNonMusicKeyword);
  if ((!videoType || !ALLOWED_VIDEO_TYPES.has(videoType)) && !shouldTreatMissingTypeAsAcceptable) {
    score += 2;
    reasons.push("invalid-video-type");
  }

  if (!Number.isFinite(confidence)) {
    score += 2;
    reasons.push("missing-confidence");
  } else if (confidence < 0.8) {
    score += 2;
    reasons.push("very-low-confidence");
  } else if (confidence < 0.9 && !isLikelyMusicNoNonMusicSignal) {
    score += 1;
    reasons.push("borderline-confidence");
  }

  if (
    parseMethod === "groq-llm"
    && Number.isFinite(confidence)
    && Math.abs(confidence - 0.8) < 1e-6
    && !isLikelyMusicNoNonMusicSignal
  ) {
    score += 1;
    reasons.push("threshold-edge-groq");
  }

  return { score, reasons };
}

function buildDeleteSql(candidates, generatedAt) {
  const ids = candidates.map((row) => row.videoId);
  const idRows = ids.map((id) => `('${sanitizeSqlString(id)}')`).join(",\n");

  return `-- Generated by scripts/scan-non-music-candidates.js\n-- Generated at ${generatedAt.toISOString()}\n-- Target count: ${ids.length}\n\nSTART TRANSACTION;\n\nDROP TEMPORARY TABLE IF EXISTS ytr_non_music_targets;\nCREATE TEMPORARY TABLE ytr_non_music_targets (\n  video_id VARCHAR(32) NOT NULL PRIMARY KEY\n);\n\nINSERT INTO ytr_non_music_targets (video_id) VALUES\n${idRows};\n\nSELECT COUNT(*) AS target_count FROM ytr_non_music_targets;\nSELECT COUNT(*) AS videos_found FROM videos v INNER JOIN ytr_non_music_targets t ON t.video_id = v.videoId;\n\n-- Delete dependent rows first to keep referential integrity.\nDELETE sv FROM site_videos sv\nINNER JOIN videos v ON v.id = sv.video_id\nINNER JOIN ytr_non_music_targets t ON t.video_id = v.videoId;\n\nDELETE av FROM videosbyartist av\nINNER JOIN videos v ON v.id = av.video_id\nINNER JOIN ytr_non_music_targets t ON t.video_id = v.videoId;\n\nDELETE pi FROM playlistitems pi\nINNER JOIN videos v ON v.id = pi.video_id\nINNER JOIN ytr_non_music_targets t ON t.video_id = v.videoId;\n\nDELETE f FROM favourites f\nINNER JOIN ytr_non_music_targets t ON t.video_id = f.videoId;\n\nDELETE m FROM messages m\nINNER JOIN ytr_non_music_targets t ON t.video_id = m.video_id;\n\nDELETE r FROM related r\nINNER JOIN ytr_non_music_targets t ON t.video_id = r.videoId OR t.video_id = r.related;\n\nDELETE v FROM videos v\nINNER JOIN ytr_non_music_targets t ON t.video_id = v.videoId;\n\nCOMMIT;\n`;
}

function isStrictDeleteCandidate(row) {
  const reasons = new Set(row.reasons);
  if (!reasons.has("non-music-keyword")) {
    return false;
  }

  // Require at least one additional weakness so we do not delete valid music
  // that merely mentions a social platform.
  return (
    reasons.has("missing-artist-or-track")
    || reasons.has("invalid-video-type")
    || reasons.has("missing-confidence")
    || reasons.has("very-low-confidence")
    || reasons.has("borderline-confidence")
  );
}

function chunkArray(items, size) {
  const chunks = [];
  for (let i = 0; i < items.length; i += size) {
    chunks.push(items.slice(i, i + size));
  }
  return chunks;
}

async function applyDeleteCandidatesLocally(videoIds) {
  const prisma = new PrismaClient();
  const totals = {
    deletedSiteVideos: 0,
    deletedArtistLinks: 0,
    deletedPlaylistItems: 0,
    deletedFavourites: 0,
    deletedMessages: 0,
    deletedRelated: 0,
    deletedVideos: 0,
  };

  const chunks = chunkArray(videoIds, 400);

  try {
    const readColumns = async (tableName) => {
      const rows = await prisma.$queryRawUnsafe(`SHOW COLUMNS FROM ${tableName}`);
      const columnSet = new Set(rows.map((row) => String(row.Field)));
      return columnSet;
    };

    const videoColumns = await readColumns("videos");
    const videosIdCol = pickColumn(videoColumns, ["id"]);
    const videosVideoIdCol = pickColumn(videoColumns, ["videoId", "video_id"]);
    if (!videosIdCol || !videosVideoIdCol) {
      throw new Error("videos table missing required id/videoId columns for apply-local delete");
    }

    const siteVideoColumns = await readColumns("site_videos");
    const siteVideosVideoRefCol = pickColumn(siteVideoColumns, ["video_id", "videoId"]);

    const artistLinkColumns = await readColumns("videosbyartist");
    const artistLinksVideoRefCol = pickColumn(artistLinkColumns, ["video_id", "videoId"]);

    const playlistItemColumns = await readColumns("playlistitems");
    const playlistItemsVideoRefCol = pickColumn(playlistItemColumns, ["video_id", "videoId"]);

    const favouritesColumns = await readColumns("favourites");
    const favouritesVideoIdCol = pickColumn(favouritesColumns, ["videoId", "video_id"]);

    const messageColumns = await readColumns("messages");
    const messagesVideoIdCol = pickColumn(messageColumns, ["video_id", "videoId"]);

    const relatedColumns = await readColumns("related");
    const relatedVideoIdCol = pickColumn(relatedColumns, ["videoId", "video_id"]);
    const relatedRelatedCol = pickColumn(relatedColumns, ["related"]);

    for (const chunk of chunks) {
      const placeholders = chunk.map(() => "?").join(",");
      const result = await prisma.$transaction(async (tx) => {
        const deletedSiteVideos = siteVideosVideoRefCol
          ? await tx.$executeRawUnsafe(
            `
              DELETE sv FROM site_videos sv
              INNER JOIN videos v ON v.${escapeIdentifier(videosIdCol)} = sv.${escapeIdentifier(siteVideosVideoRefCol)}
              WHERE v.${escapeIdentifier(videosVideoIdCol)} IN (${placeholders})
            `,
            ...chunk,
          )
          : 0;

        const deletedArtistLinks = artistLinksVideoRefCol
          ? await tx.$executeRawUnsafe(
            `
              DELETE av FROM videosbyartist av
              INNER JOIN videos v ON v.${escapeIdentifier(videosIdCol)} = av.${escapeIdentifier(artistLinksVideoRefCol)}
              WHERE v.${escapeIdentifier(videosVideoIdCol)} IN (${placeholders})
            `,
            ...chunk,
          )
          : 0;

        const deletedPlaylistItems = playlistItemsVideoRefCol
          ? await tx.$executeRawUnsafe(
            `
              DELETE pi FROM playlistitems pi
              INNER JOIN videos v ON v.${escapeIdentifier(videosIdCol)} = pi.${escapeIdentifier(playlistItemsVideoRefCol)}
              WHERE v.${escapeIdentifier(videosVideoIdCol)} IN (${placeholders})
            `,
            ...chunk,
          )
          : 0;

        const deletedFavourites = favouritesVideoIdCol
          ? await tx.$executeRawUnsafe(
            `DELETE FROM favourites WHERE ${escapeIdentifier(favouritesVideoIdCol)} IN (${placeholders})`,
            ...chunk,
          )
          : 0;

        const deletedMessages = messagesVideoIdCol
          ? await tx.$executeRawUnsafe(
            `DELETE FROM messages WHERE ${escapeIdentifier(messagesVideoIdCol)} IN (${placeholders})`,
            ...chunk,
          )
          : 0;

        const relatedPlaceholders = chunk.map(() => "?").join(",");
        const deletedRelated = (relatedVideoIdCol && relatedRelatedCol)
          ? await tx.$executeRawUnsafe(
            `DELETE FROM related WHERE ${escapeIdentifier(relatedVideoIdCol)} IN (${placeholders}) OR ${escapeIdentifier(relatedRelatedCol)} IN (${relatedPlaceholders})`,
            ...chunk,
            ...chunk,
          )
          : 0;

        const deletedVideos = await tx.$executeRawUnsafe(
          `DELETE FROM videos WHERE ${escapeIdentifier(videosVideoIdCol)} IN (${placeholders})`,
          ...chunk,
        );

        return {
          deletedSiteVideos: Number(deletedSiteVideos ?? 0),
          deletedArtistLinks: Number(deletedArtistLinks ?? 0),
          deletedPlaylistItems: Number(deletedPlaylistItems ?? 0),
          deletedFavourites: Number(deletedFavourites ?? 0),
          deletedMessages: Number(deletedMessages ?? 0),
          deletedRelated: Number(deletedRelated ?? 0),
          deletedVideos: Number(deletedVideos ?? 0),
        };
      });

      totals.deletedSiteVideos += result.deletedSiteVideos;
      totals.deletedArtistLinks += result.deletedArtistLinks;
      totals.deletedPlaylistItems += result.deletedPlaylistItems;
      totals.deletedFavourites += result.deletedFavourites;
      totals.deletedMessages += result.deletedMessages;
      totals.deletedRelated += result.deletedRelated;
      totals.deletedVideos += result.deletedVideos;
    }

    return totals;
  } finally {
    await prisma.$disconnect();
  }
}

async function main() {
  loadDatabaseEnv();

  if (!process.env.DATABASE_URL) {
    throw new Error("DATABASE_URL is not set.");
  }

  const minScore = Math.max(1, Number(parseArg("min-score", "4")) || 4);
  const rawMaxCandidates = Number(parseArg("max-candidates", "500"));
  const maxCandidates = Number.isFinite(rawMaxCandidates) && rawMaxCandidates > 0
    ? Math.floor(rawMaxCandidates)
    : Number.MAX_SAFE_INTEGER;
  const batchSize = Math.max(100, Math.min(5000, Number(parseArg("batch-size", "2000")) || 2000));
  const scanAll = ["1", "true", "yes"].includes(String(parseArg("scan-all", "0")).toLowerCase());
  const scanScope = String(parseArg("scope", "available")).toLowerCase() === "all-videos"
    ? "all-videos"
    : "available";
  const applyLocal = ["1", "true", "yes"].includes(String(parseArg("apply-local", "0")).toLowerCase());
  const confirmDelete = String(parseArg("confirm-delete", ""));

  if (applyLocal && confirmDelete !== "YES") {
    throw new Error("Refusing to delete without explicit confirmation. Re-run with --apply-local=1 --confirm-delete=YES");
  }

  const prisma = new PrismaClient();
  const candidates = [];

  let lastSeenId = Number.MAX_SAFE_INTEGER;
  let scannedRows = 0;
  let totalRowsScanned = 0;
  let hasMore = true;

  try {
    while (hasMore && (scanAll || candidates.length < maxCandidates)) {
      const rows = await prisma.$queryRawUnsafe(
        scanAll
          ? `
            SELECT
              v.id,
              v.videoId,
              v.title,
              v.description,
              v.parsedArtist,
              v.parsedTrack,
              v.parsedVideoType,
              v.parseMethod,
              v.parseConfidence
            FROM videos v
            WHERE v.id < ?
              ${scanScope === "available"
                ? `AND EXISTS (
                    SELECT 1
                    FROM site_videos sv
                    WHERE sv.video_id = v.id
                      AND sv.status = 'available'
                  )`
                : ""}
            ORDER BY v.id DESC
            LIMIT ?
          `
          : `
            SELECT
              v.id,
              v.videoId,
              v.title,
              v.description,
              v.parsedArtist,
              v.parsedTrack,
              v.parsedVideoType,
              v.parseMethod,
              v.parseConfidence
            FROM videos v
            WHERE v.id < ?
              ${scanScope === "available"
                ? `AND EXISTS (
                    SELECT 1
                    FROM site_videos sv
                    WHERE sv.video_id = v.id
                      AND sv.status = 'available'
                  )`
                : ""}
              AND (
                v.parsedArtist IS NULL OR TRIM(v.parsedArtist) = '' OR
                v.parsedTrack IS NULL OR TRIM(v.parsedTrack) = '' OR
                v.parsedVideoType IS NULL OR v.parsedVideoType = 'unknown' OR
                v.parseConfidence IS NULL OR v.parseConfidence < 0.9 OR
                LOWER(v.title) REGEXP 'instagram|tiktok|facebook|whatsapp|snapchat|podcast|interview|prank|challenge|reaction|vlog|tutorial|gameplay|livestream|stream highlights?|news|fail|compilation|meme|shorts?'
              )
            ORDER BY v.id DESC
            LIMIT ?
          `,
        lastSeenId,
        batchSize,
      );

      if (!rows.length) {
        hasMore = false;
        break;
      }

      scannedRows += rows.length;
      totalRowsScanned += rows.length;

      for (const row of rows) {
        const { score, reasons } = scoreCandidate(row);
        if (score >= minScore) {
          candidates.push({
            id: row.id,
            videoId: row.videoId,
            title: row.title,
            parsedArtist: row.parsedArtist,
            parsedTrack: row.parsedTrack,
            parsedVideoType: row.parsedVideoType,
            parseMethod: row.parseMethod,
            parseConfidence: row.parseConfidence,
            score,
            reasons,
          });

          if (!scanAll && candidates.length >= maxCandidates) {
            break;
          }
        }
      }

      lastSeenId = Number(rows[rows.length - 1].id);
      if (rows.length < batchSize) {
        hasMore = false;
      }
    }
  } finally {
    await prisma.$disconnect();
  }

  const deduped = new Map();
  for (const row of candidates) {
    if (!deduped.has(row.videoId)) {
      deduped.set(row.videoId, row);
    }
  }

  const sortedCandidates = [...deduped.values()]
    .sort((a, b) => b.score - a.score || b.id - a.id);
  const wasTruncated = sortedCandidates.length > maxCandidates;
  const reviewCandidates = sortedCandidates.slice(0, maxCandidates);

  const deleteCandidates = reviewCandidates.filter(isStrictDeleteCandidate);
  const deleteIds = deleteCandidates.map((row) => row.videoId);
  let localDeletionSummary = null;

  if (applyLocal && deleteIds.length > 0) {
    localDeletionSummary = await applyDeleteCandidatesLocally(deleteIds);
  }

  const generatedAt = new Date();
  const stamp = generatedAt.toISOString().replace(/[:.]/g, "-");
  const outDir = path.resolve(process.cwd(), "logs");
  fs.mkdirSync(outDir, { recursive: true });

  const jsonPath = path.join(outDir, `non-music-candidates-${stamp}.json`);
  const strictIdsPath = path.join(outDir, `non-music-candidates-${stamp}-strict-ids.txt`);
  const reviewTitlesPath = path.join(outDir, `non-music-candidates-${stamp}-review-titles.txt`);
  const sqlPath = path.join(outDir, `non-music-candidates-${stamp}-vps-delete.sql`);

  const payload = {
    generatedAt: generatedAt.toISOString(),
    scannedRows,
    totalRowsScanned,
    scanAll,
    scanScope,
    minScore,
    maxCandidates,
    wasTruncated,
    candidateCount: reviewCandidates.length,
    deleteCandidateCount: deleteCandidates.length,
    applyLocal,
    localDeletionSummary,
    candidates: reviewCandidates,
    deleteCandidates,
  };

  fs.writeFileSync(jsonPath, JSON.stringify(payload, null, 2));
  fs.writeFileSync(strictIdsPath, `${deleteCandidates.map((row) => row.videoId).join("\n")}\n`);
  const reviewTitles = reviewCandidates
    .map((row) => String(row.title ?? "").replace(/[\r\n]+/g, " ").trim())
    .filter(Boolean)
    .join("\n");
  fs.writeFileSync(reviewTitlesPath, `${reviewTitles}\n`);

  if (deleteCandidates.length > 0) {
    fs.writeFileSync(sqlPath, buildDeleteSql(deleteCandidates, generatedAt));
  }

  console.log(`Scanned rows: ${scannedRows}`);
  console.log(`Scan mode: ${scanAll ? `full pass (${scanScope})` : `prefiltered candidate window (${scanScope})`}`);
  console.log(`Review candidates found: ${reviewCandidates.length}`);
  console.log(`Strict delete candidates found: ${deleteCandidates.length}`);
  if (wasTruncated) {
    console.log(`Output truncated at max-candidates=${maxCandidates}. Increase --max-candidates for a wider review set.`);
  }
  console.log(`JSON report: ${path.relative(process.cwd(), jsonPath)}`);
  console.log(`Strict ids list: ${path.relative(process.cwd(), strictIdsPath)}`);
  console.log(`Review titles list: ${path.relative(process.cwd(), reviewTitlesPath)}`);
  if (deleteCandidates.length > 0) {
    console.log(`VPS delete SQL: ${path.relative(process.cwd(), sqlPath)}`);
  } else {
    console.log("No SQL generated because no candidates met threshold.");
  }

  if (applyLocal) {
    if (localDeletionSummary) {
      console.log("Local delete summary:");
      console.log(`  site_videos: ${localDeletionSummary.deletedSiteVideos}`);
      console.log(`  videosbyartist: ${localDeletionSummary.deletedArtistLinks}`);
      console.log(`  playlistitems: ${localDeletionSummary.deletedPlaylistItems}`);
      console.log(`  favourites: ${localDeletionSummary.deletedFavourites}`);
      console.log(`  messages: ${localDeletionSummary.deletedMessages}`);
      console.log(`  related: ${localDeletionSummary.deletedRelated}`);
      console.log(`  videos: ${localDeletionSummary.deletedVideos}`);
    } else {
      console.log("Local delete summary: nothing deleted.");
    }
  }
}

main().catch((error) => {
  console.error(error instanceof Error ? error.message : String(error));
  process.exit(1);
});
